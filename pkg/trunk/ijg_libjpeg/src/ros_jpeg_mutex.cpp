#include "ijg_libjpeg/ros_jpeg_mutex.h"
#include "ros/ros_mutex.h"

// this is probably the dumbest library I've ever written.

static ROS_Mutex jpeg_mutex;

void ros_jpeg_mutex_lock()
{
  jpeg_mutex.lock();
}

void ros_jpeg_mutex_unlock()
{
  jpeg_mutex.unlock();
}

